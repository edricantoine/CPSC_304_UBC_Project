package group.project.model;

public class Player4Model {
    private final int exp;
    private final int mana;

    public Player4Model(int exp, int mana) {
        this.exp = exp;
        this.mana = mana;
    }

    public int getExp() {
        return exp;
    }

    public int getMana() {
        return mana;
    }
}
